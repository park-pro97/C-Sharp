//직업별 인원 수
SELECT JOB, COUNT(*) FROM EMP
GROUP BY JOB;

-------------------------------------------------------------------------------------------------
//조건 부서 번호 개수
SELECT DEPTNO, COUNT(*) FROM EMP
WHERE DEPTNO = 20
GROUP BY DEPTNO
HAVING COUNT(*) >= 3;


-------------------------------------------------------------------------------------------------
//급여가 2000이상 3000 이하인 사람의 직업과 부서번호 출력(이 문법 중요)
SELECT DEPTNO, JOB, AVG(SAL)
FROM EMP
WHERE SAL <= 3000
GROUP BY DEPTNO, JOB
HAVING AVG(SAL) >=2000;

ORDER BY DEPTNO; -- 마지막에 부서 번호 순으로 정렬시키기
