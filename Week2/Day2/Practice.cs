//do while문으로 구구단 출력
namespace DoWhile
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int i = 1;
            do
            {
                Console.WriteLine($"3 * {i} = {3 * i} ");
                i++;
            } while (i < 10);
            
        }
    }
}

